                        <p class="article">Convidada pelo Seac-MS (Sindicato das Empresas de Asseio e Conservação do Estado de Mato Grosso do Sul), a ANSEGTV acompanhou na última terça-feira (25) palestra do economista e professor Marcos Cintra. O evento também teve o apoio da Febrac (Federação Nacional das Empresas Prestadoras de Serviços de Limpeza e Conservação) e da Fecomércio-MS (Federação do Comércio de Bens, Serviços e Turismo do Mato Grosso do Sul). </p>

                        <p class="article">Considerado um dos maiores especialistas na área, Cintra traçou um panorama sobre a tramitação da Reforma Tributária.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/05/evento-reforma-tributaria-2021.jpg" width="450" height="auto" alt="Reforma Tributária" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">O especialista alertou que a criação do IBS (Imposto sobre Bens e Serviços) com alíquota de 25%, substituindo o PIS, COFINS, IPI (tributos federais), ICMS (estadual) e ISS (municipal) por um modelo de tributação de Imposto sobre Valor Agregado (IVA) seria extremamente prejudicial para o setor de serviços. que teria sua carga tributária aumentada de forma exponencial.</p>

                        <p class="article">Cintra considera improvável o consenso em torno de uma Reforma Tributária ampla ainda neste ano. Mas avalia como possível a aprovação de projetos de lei sobre o tema. Um deles, o PL 3.887/2020, que cria a CBS (Contribuição Social sobre Operações com Bens e Serviços) e extingue o PIS e a COFINS, também seria negativo para o setor de serviços, destacou.</p>

                        <p class="article">Ainda durante sua explanação, Cintra defendeu a desoneração da folha de pagamentos como forma de aliviar o setor de serviços. O economista ainda ressaltou a importância da Reforma Administrativa, que proporcionaria maior enxugamento do Estado e menos burocracia.</p>
