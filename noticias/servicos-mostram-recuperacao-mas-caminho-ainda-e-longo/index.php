                        <p class="article">De acordo com o IBGE (Instituto Brasileiro de Geografia e Estatística), o volume de serviços no país cresceu 0,7% em abril, na comparação com março. O número representa uma melhora em relação à queda de 3,1% em março.</p>

                        <p class="article">No acumulado do ano, o índice tem alta de 3,7%. Em 12 meses ainda está negativo, mas em evolução, ao passar de -8% em março para -5,4% em abril.</p>

                        <p class="article">Na divisão por regiões, 12 estados e o Distrito Federal apresentaram alta. São Paulo (0,5%), DF (4,8%) e Paraná (1,5%) tiveram os melhores resultados. Minas Gerais (-1%) e Mato Grosso (-2,4%) registraram as maiores quedas.</p>

                        <p class="article">Os sinais de recuperação são claros, mas o caminho ainda é longo. Importante lembrar que o setor foi um dos mais atingidos pela pandemia, com o fechamento de lojas, restaurantes e as restrições de circulação.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/06/grafico-indicadores.png" width="450" height="auto" alt="Gráfico indicador de crescimento" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>
                         
                        <p class="article">Obviamente, uma retomada sólida e contínua depende da vacinação maciça da população. Nos últimos dias, os governos estaduais vêm anunciando uma maior aceleração no processo de imunização.</p>

                        <p class="article">A confirmação dessa expectativa é de vital importância. Apenas assim ocorrerá um retorno seguro às atividades de produção e consumo.</p>

                        <p class="article">Não podemos esquecer ainda da necessidade da aprovação de reformas estruturais, como a tributária e a administrativa. Essas é que pavimentarão o caminho para um futuro melhor.</p>

                        <p class="article">Programas do governo federal de incentivo às empresas e que fomentem a criação e manutenção de empregos também são sempre bem-vindos.</p>

                        <p class="article">Com a colaboração de todos - governantes, congressistas e a população - poderemos ter um segundo semestre com números ainda mais positivos.</p>

