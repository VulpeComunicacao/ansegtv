                            <p class="article"><em>Em entrevista, presidente da ANSEGTV avalia momento atual do mercado e fala sobre desafios e expectativas para o ano</em></p>

                            <p class="article">Maior eficiência, geração de valor para o cliente e adequação da operação à nova realidade do mercado. Essas são algumas das ações que o presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno, acredita serem necessárias para que empresas do setor de transporte de valores e segurança privada obtenham bons resultados hoje em dia. Na entrevista a seguir, Damasceno comenta importantes temas que influenciam no cotidiano dos profissionais da área e da população em geral.</p>

                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2023/02/gabriel-presidente-ansegtv.png" width="450" height="auto" alt="Diretor da ANSEGTV - Gabriel" class=" img-fluid">	
