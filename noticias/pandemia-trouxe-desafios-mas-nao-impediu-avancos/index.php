                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">Seria impossível fazer um balanço do segundo ano de atividades da ANSEGTV sem falar dos impactos da pandemia. Fundada em fevereiro de 2019, a entidade soube se adaptar e criar alternativas para seguir atuando de forma produtiva e efetiva. Na entrevista a seguir, o presidente Gabriel Damasceno aborda as dificuldades enfrentadas, as conquistas obtidas e os desafios que vêm pela frente.</p>
                            <p class="article"><strong>- Como a pandemia afetou ou influenciou os planos da ANSEGTV em seu segundo ano?</strong></p>
                            <p class="article">2020 seria muito importante para o fortalecimento do planejamento estratégico institucional da entidade, pois seria o ano para interagir mais ativamente junto às autoridades públicas e mostrar a importância da representação nacional de empresas de transporte de valores e segurança privada, alinhadas aos princípios democráticos, éticos e de boas práticas concorrenciais. Ainda assim, apesar das medidas de isolamento impostas pela pandemia, o crescimento institucional ocorreu e se deu, principalmente, pela parceria com a Frente Parlamentar do Serviços, que reúne associações do setor, engajadas na defesa do segmento que mais emprega no Brasil. Graças a esse trabalho conjunto houve também uma boa comunicação junto às autoridades do Poder Executivo e aos parlamentares para apresentar nossos pleitos legítimos pela sobrevivência financeira das empresas e manutenção de milhares de empregos mesmo durante a pandemia.</p>
                            <p class="article">A ANSEGTV também teria realizado mais encontros presenciais internos, porém a pandemia nos mostrou que as reuniões virtuais são possíveis. O número de encontros, mesmo à distância, ficou próximo do ano anterior e foram bastante produtivos.</p>
                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/02/gabriel-presidente-ansegtv.png" width="450" height="auto" alt="Gabriel - Presidente da ANSEGTV" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article"><strong>- O home office mostrou-se bastante produtivo. Quando a situação voltar ao normal, é possível adotar a modalidade em algumas situações?</strong></p>
                            <p class="article">Sim, o modelo remoto veio para ficar e sua aplicação varia de acordo com o tipo de trabalho. Não é possível home office para a atividade operacional de transporte de valores, por exemplo. Mas para algumas tarefas realizadas dentro das empresas do nosso setor é possível. Nós sabemos que os legisladores podem melhorar ainda mais essa realidade do trabalho remoto aprovando mudanças na legislação trabalhista que viabilizem o melhor formato para empregados e empregadores. A ANSEGTV acompanha proposições legislativas e normas executivas que buscam aperfeiçoar o ambiente de trabalho e melhorar o desenvolvimento das atividades de vigilância e transporte de numerário no país.</p>

                            <p class="article"><strong>- O sr. é favorável a mais medidas de incentivo à economia e à manutenção de empregos? Quais, por exemplo?</strong></p>
                            <p class="article">Certamente! A Associação é favorável às políticas públicas de manutenção e geração de empregos, além do fortalecimento das atividades do segmento de serviços. O modelo do Programa BEM (Programa Emergencial de Manutenção do Emprego e da Renda) criado pelo governo durante a pandemia que permitiu a suspensão temporária e a redução proporcional de jornada e salário foi essencial para que as empresas conseguissem sobreviver nesse momento tão crítico. Medidas como prorrogação e parcelamento de tributos também ajudaram. Nós da ANSEGTV esperamos que o governo se mantenha sensível à responsabilidade que as empresas têm em proporcionar desenvolvimento econômico. Muito já foi feito, mas sempre podemos avançar um pouco mais. Por exemplo, com mais linhas de créditos, com leis e regras claras que geram segurança jurídica, e claro, com as reformas imprescindíveis para a retomada do crescimento econômico e social, como a tributária.</p>
                            <p class="article"><strong>- Qual a expectativa para este ano em torno do trabalho da Frente Parlamentar do Serviços?</strong></p>
                            <p class="article">A melhor possível sempre! A ANSEGTV sente-se honrada por integrar o grupo de apoio formado por entidades representativas do setor de serviços responsável por subsidiar tecnicamente os deputados na aprovação de proposições importantes para todo o país. A Frente fez muito em 2020 e tem planos para fazer ainda mais em 2021, visto que as atividades legislativas serão, e já estão mais intensas em 2021. Os novos presidentes da Câmara e do Senado já demonstraram que querem colocar em votação medidas importantes para o desenvolvimento do Brasil. A ANSEGTV está à disposição da Frente para colaborar ainda mais em 2021 e agradece ao deputado Laércio Oliveira (PP-SE) pela iniciativa de formar a Frente e por atuar no fortalecimento das atividades do segmento de serviços.</p>
                            <p class="article"><strong>- O que o sr. diria para uma empresa interessada em se associar à entidade?</strong></p>
                            <p class="article">Toda empresa do segmento de transporte de valores, que se alinha aos ideais e à missão da ANSEGTV, é bem-vinda na nossa entidade. Vale destacar nosso crescimento. As empresas associadas representam em conjunto cerca de 20% das operações do setor. Isso demonstra que estamos no caminho certo quando defendemos um ambiente concorrencial livre e equilibrado. A concorrência desleal é prejudicial para o país porque compromete a qualidade e a segurança dos serviços prestados, além de inibir o crescimento econômico, concentrar o mercado, aumentar os preços e desestimular a inovação.</p>
                            <p class="article"><strong>- Muito se fala sobre o fim do dinheiro em espécie, mas a pandemia mostrou como ele ainda é importante. Como vê essa questão?</strong></p>
                            <p class="article">A circulação de dinheiro cresce a cada mês. O pagamento do auxílio emergencial pelo governo federal contribuiu ainda mais para isso. Graças à moeda física, a economia seguiu girando. A criação da nota de R$ 200 comprovou esse movimento. Claro que os meios de pagamento eletrônico são uma realidade. Mas é possível uma convivência harmônica e saudável entre todas as formas de transferência de valores.</p>
                            <p class="article"><strong>- Quais os principais planos da ANSEGTV para 2021?</strong></p>
                            <p class="article">Fortalecer as relações com os órgãos públicos, como Polícia Federal, Secretarias de Segurança Pública, sindicatos, entre outros. Também devemos criar uma agenda positiva sobre temas relacionados ao setor de transporte de valores. Os esforços para incentivar a livre concorrência deverão estar sempre entre nossas prioridades. Ainda planejamos desenvolver estudos na área para enriquecer a gestão estratégica e operacional das empresas.</p>                            
