                            <p class="article">A Associação Nacional de Segurança e Transporte de Valores (Ansegtv) realizou duas reuniões com representantes de resseguradoras em Londres na sexta-feira, dia 11 de outubro de 2019. A comitiva foi formada pelo presidente da Ansegtv Gabriel Damasceno, diretores e membros das empresas associadas. A missão teve o objetivo de apresentar ao mercado de resseguros a ANSEGTV, seus objetivos, sua abrangência e as particularidades do momento atual do segmento de transporte de valores no Brasil.</p>
                                                            
                            <p class="article">Os investimentos realizados pelas empresas associadas em medidas de defesa, buscando reduzir os riscos e aumentar a segurança de suas operações, foram um dos principais pontos destacados. Os resultados dessas medidas ficaram claramente demonstrados pelo baixo índice de sinistralidade nos últimos cinco anos das transportadoras que fazem parte da associação.</p>  
                                
                            <p class="article">A ANSEGTV mostrou ainda como o crescimento significativo do valor cobrado na renovação do seguro onera as empresas pequenas e médias ao ponto de poder inviabilizá-las. No encontro foi exposto a necessidade de avaliar de forma individual o modelo de operação de cada empresa, bem como as respectivas regiões de atuação, visando uma cobrança compatível com o nível de risco de cada player deste setor.</p>
                            
                            <p class="text-center"> <img src="../../img/uploads/2019/11/marsh-jlt-office.png" width="490" height="auto" alt="Time ANSEGTV no Marsh JLT OFFICE" class="m-2 img-fluid "></p>
                                
                            <p class="article">Continuaremos com a nossa missão de promover as melhores práticas de segurança e qualidade nos serviços oferecidos ao mercado, fomentar a ética e a livre concorrência nas relações comerciais e apoiar a modernização da legislação do setor de segurança no Brasil.</p>  
