                        <p class="article">O Índice de Serviços (ISe) registrou crescimento de 3,3% em julho deste ano. A taxa foi criada em junho pela Frente Parlamentar do Setor de Serviços (FPS), que conta com o apoio de diversas entidades do setor, entre elas a ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</p>

                        <p class="article">Quando comparado ao mesmo mês de 2020, a alta do ISe foi de 12,1%. A variação no acumulado do ano, de janeiro a julho de 2021, ante igual período do ano passado, foi de 6,5%.</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/10/grafico.jpg" width="450" height="auto" alt="Gráfico apontado para cima" class=" img-fluid">	
                            <figcaption>J</figcaption>
                        </figure>

                        <p class="article">A alta do ISe está relacionada aos aumentos da receita, de volume e da mão de obra do setor, que cresceram, respectivamente, 6,28%, 5,22% e 0,84%</p>

                        <p class="article">Entre os componentes do ISe, o nível de emprego é o único que ainda não atingiu o patamar pré-pandemia e se encontra 2,7% abaixo do valor registrado em fevereiro de 2021. 
                        </p>

                        <p class="article">Ainda de acordo com a FPS, no Brasil, já há entendimento em sentido similar,conforme Emenda Constitucional nº 42, de 2003, que prevê a redução de carga tributária sobre o emprego como uma das razões primordiais para a criação de um regime não cumulativo aplicável às contribuições sociais.</p>
                      
                        <p class="article">O novo indicador tem como objetivo avaliar mensalmente a capacidade depromoção de desenvolvimento sustentado no setor que responde por mais de 60% do PIB e por 57% dos empregos formais em todo país.</p>

                        <p class="article">Segundo o presidente da FPS, deputado Laércio Oliveira (PP/SE), hoje há umcenário de forte aumento de desemprego e de fome, que é acompanhado pela crescente inflação. Para ele, em períodos como esse, políticas públicas adequadas são mais do que urgentes.</p>
