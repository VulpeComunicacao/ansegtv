                        <p class="article">A CGCSP (Coordenação-Geral de Controle de Serviços e Produtos da Polícia Federal) promoveu no dia 17 de março o Seminário Nacional de PLD/FT (Prevenção à Lavagem de Dinheiro e Financiamento ao Terrorismo).</p>


                        <figure class="pull-right m-2 text-right">
                            <img src="../../img/uploads/2022/03/seminario-nacional.jpg" width="450" height="auto" alt="Seminário Nacional ANSEGTV" class=" img-fluid">	
