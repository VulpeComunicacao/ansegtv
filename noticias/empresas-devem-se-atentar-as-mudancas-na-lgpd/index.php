                            <h1>Empresas devem se atentar às mudanças na LGPD, mas multas e sanções aguardam
regulamentação</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 05/08/2021 - 14h55</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
