                            <h1>ANSEGTV contribui para inovações tecnológicas apresentadas no 6º Inova NFC-e</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 30/07/2021 - 15h10</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
