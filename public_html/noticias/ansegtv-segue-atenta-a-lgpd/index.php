                            <p class="article">Tema de suma importância, a LGPD (Lei Geral de Proteção de Dados) vem sendo debatida constantemente pela ANSEGTV e seus associados.</p>
                            <p class="article">A entidade, com o apoio de consultorias jurídicas especialistas no tema, tem buscado prestar todos os esclarecimentos aos filiados.</p>
                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/02/img-lgpd.png" width="450" height="auto" alt="Imagem da LGPD referenciando proteção de dados com um cadeado sobre o computador" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">No dia 28 de janeiro, uma cartilha com os principais pontos da legislação foi apresentada às empresas que integram a associação.</p>
                            <p class="article">O material traz as adequações indispensáveis para que, tanto ANSEGTV quanto seus membros, cumpram corretamente as determinações da lei.</p>
                            <p class="article">Sancionada em 14 de agosto de 2018, a LGPD entrou em vigor em 18 de setembro de 2020. A discussão no Congresso Nacional sobre o tema durou quase uma década (2010-2018).</p>
                            <p class="article">Os artigos que disciplinam as sanções administrativas da lei entrarão em vigor apenas em agosto de 2021, mas empresas e administração pública vêm se movimentando de maneira significativa para ficar em conformidade com a norma.</p>
                            <p class="article">No transporte de valores não é diferente. Todas as empresas que, de alguma forma, tratem dados pessoais, como por exemplo Recursos Humanos, Jurídico, Logística, Contábil, Marketing e Compliance devem se ajustar às novas diretrizes.</p>
                            <p class="article">Por isso, o suporte de uma consultoria especializada nesse momento é vital. Com ela, é possível avaliar dados, riscos e vantagens da implementação da lei.</p>
                            <p class="article">A ANSEGTV seguirá contribuindo e apoiando todos os associados na busca por uma eficaz e célere adaptação à LGPD.</p>
