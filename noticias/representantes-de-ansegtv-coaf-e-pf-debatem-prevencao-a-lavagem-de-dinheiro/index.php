                            <h1>Representantes de ANSEGTV, COAF e PF debatem prevenção à lavagem de dinheiro</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> ANSEGTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>31/10/2023 - 10h25</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
