                        <p class="article">O Ministério da Justiça prepara uma Proposta de Emenda de Constituição (PEC) que permitirá ao governo federal estabelecer diretrizes nacionais a serem seguidas por estados e municípios na segurança pública.</p>

                        <p class="article">Em entrevista à Globonews, o secretário Nacional de Segurança Pública, Mario Luiz Sarrubbo, disse que o objetivo é ampliar a capacidade de atuação da União nessa área, que é competência principalmente dos governos estaduais, sem interferir na capacidade de eles poderem estabelecer normas que digam respeito às peculiaridades locais.
</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2024/05/palacio-justica.png" width="450" height="auto" alt="Palácio da Justiça - Brasília" class=" img-fluid">	
                                <figcaption</figcaption>
                        </figure>

                        <p class="article">A PEC, cuja minuta já estaria pronta, mas passará ainda pelo presidente Lula antes de ser apresentada ao Congresso, deve propor também a ampliação da atuação da Polícia Federal (PF) no combate ao crime organizado. </p>

                        <p class="article">A ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) acompanha com atenção a elaboração da proposta e é favorável ao maior poder da PF para combater o crime organizado. </p>

                        <p class="article">Nas oportunidades de diálogo institucional com a Secretaria Nacional de Segurança Pública, a entidade tem manifestado em diversas ocasiões apoio às políticas que fomentem a integração entre as forças de segurança, o que é fundamental para garantir uniformidade da segurança pública no Brasil.</p>



                        



                       

