                        <p class="article">O diretor jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Max Tavares Madruga, participou, no último dia 17 de maio, de uma reunião com o diretor executivo da CNT (Confederação Nacional do Transporte), Bruno Batista, para debater pautas comuns para o setor de transporte.  A CNT é uma importante interlocutora das demandas do setor e tem pautado discussões de interesse com os poderes Executivo, Legislativo e Judiciário.</p>

                        <p class="article"> Na ocasião, a CNT apresentou estudos e painéis técnicos produzidos pela equipe e, dentre eles, um importante levantamento acerca da localização, da quantidade, do tipo e da densidade de pontos críticos registrados nas rodovias federais e estaduais do país. </p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2023/05/reuniao-cnt.png" width="450" height="auto" alt="Reunião com a equipe da CNT" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article"> O diretor Max Tavares Madruga também discorreu sobre o cenário do transporte e logística de valores no Brasil. As duas instituições avaliam, inclusive, a possibilidade de realizar estudos em conjunto de interesse do setor transportador e do país.</p>

                        <p class="article"> “O setor de transporte move nosso País e a CNT defende os interesses da atividade transportadora. Por isso, a importância em aproximar nossas pautas e trocar conhecimento técnico sobre transporte e, sobretudo, segurança”, destacou Max Tavares Madruga.</p>

                        <p class="article"> Participaram também do encontro pela CNT: Jefferson Cristiano, gerente executivo de estatística e pesquisa, Fernanda Schwantes, gerente executiva de economia e Erica Marcos, gerente executiva ambiental.</p>


                      
