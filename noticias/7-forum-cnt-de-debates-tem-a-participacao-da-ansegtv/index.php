                        <p class="article">O Diretor Jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Max Tavares Madruga, representou a entidade no 7º Fórum CNT de Debates – Segurança Pública e a Atividade Transportadora, realizado no dia 22 de novembro, em Brasília.</p>

                        <p class="article">O evento reuniu empresários de todos os setores do transporte, autoridades nacionais e especialistas para debater os desafios e propor soluções para a segurança pública no segmento. </p>
                        
                        <p class="article">Dentre os participantes, estiveram o presidente do Sistema Transporte, Vander Costa; o ministro da Justiça e Segurança Pública (MJSP), Flávio Dino; e o diretor da OEI (Organização dos Estados Ibero-americanos para a Educação, a Ciência e a Cultura) no Brasil, Leonardo Barchini.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2023/11/7-forum.png" width="450" height="auto" alt="Representantes da ANSEGTV, COAF e PF" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">Em sua fala, o ministro Flávio Dino elencou as principais ações desenvolvidas pela sua pasta ao longo de 2023, para coibir roubo de cargas, tráfico de drogas, depredação de patrimônio e outras práticas que trazem prejuízos para o setor.</p>

                        <p class="article">“Os dados mostram uma queda na ocorrência de incidentes, mas isso não se reflete na percepção das pessoas. Então, nós não temos um problema quantitativo, mas, sim, qualitativo. O problema está na configuração dos crimes. Hoje, não se fala mais de um ‘crime de varejo’, e, sim, de imensas cadeias produtivas articuladas com ‘atacadistas do crime’”, disse Dino.</p>

                        <p class="article">Além do evento na parte da manhã, um grupo de trabalho coordenado pela SENASP (Secretaria Nacional de Segurança Pública), com o apoio da CNT, se reuniu, na parte da tarde, para debater a criação de planos de ação para o combate aos crimes no setor de transporte. Este grupo contou com representantes da segurança pública de todo o país e entidades privadas que representam o setor de transporte, inclusive a ANSEGTV</p>

                        <p class="article">“Foi um evento muito enriquecedor. É importante esse diálogo com autoridades e representantes do setor de transportes. Seguimos à disposição para contribuir com ideias e propostas“, afirmou Max.</p>

                        

                        


