                        <p class="article"> Representantes da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) e do Ministério da Justiça estiveram reunidos no último dia 9 de junho, em Brasília.</p>

                        <p class="article"> Pela entidade, participaram o Diretor Jurídico, Max Madruga, o Diretor de Relações Governamentais, Lincoln Telhado, e o advogado Mauricio Wanderley. Eles foram recebidos pelo assessor especial do Ministro da Justiça, Allan Dias, pelo Assessor do Secretário-Executivo, Frederico dos Santos, e pelo Chefe da Divisão de Processos Autorizativos de Segurança Privada da Polícia Federal, Guilherme Lopes Maddarena. </p>
                       
                        <figure class="pull-right m-2 text-right">
                            <img src="../../img/uploads/2022/06/equipe-mjustica.png" width="450" height="auto" alt="Audiência no Ministério da Justiça" class=" img-fluid">	
                            <figcaption></figcaption>
                        </figure>


                        <p class="article"> No encontro, a ANSEGTV apresentou sugestões de seus associados para possíveis melhorias na Portaria nº 3233/2012, que dispõe sobre as normas relacionadas às atividades de segurança privada. Max também apresentou um panorama atual do setor no ponto de vista das empresas, bem como os esforços empregados para o combate ao crime organizado. </p>

                        <p class="article"> Os integrantes do ministério ressaltaram a importância do diálogo do governo e da Polícia Federal com as empresas de segurança privada, especialmente para a melhoria da segurança pública como um todo.</p>

                        <p class="article"> Max avaliou a reunião como bastante produtiva. "Os integrantes do Ministério da Justiça foram muito receptivos. A troca de informações foi bem enriquecedora. Pudemos apresentar nossas sugestões e também ouvir o que vem sendo feito para aperfeiçoar a atividade de segurança privada", disse.</p>
                        
