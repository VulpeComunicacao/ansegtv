                        <p class="article">O deputado federal Júlio Lopes (PP-RJ), coordenador do GT LGPD (Lei Geral de
                        Proteção de Dados Pessoais) da FPS (Frente Parlamentar do Setor de
                        Serviços), reuniu-se na terça-feira (31) com o presidente da ANPD
                        (Autoridade Nacional de Proteção de Dados), Waldemar Gonçalves Ortunho
                        Junior.</p>

                        <p class="article">A reunião teve como objetivo apresentar as prioridades do setor de serviços
                        relacionadas à legislação sobre o tema e demonstrar apoio institucional à
                        ANPD no Congresso Nacional quanto aos seguintes assuntos: participação em
                        audiências públicas e resultados auferidos; construção de entendimentos no
                        Legislativo para gerar protagonismo à ANPD; e apoio à certificação
                        desenvolvida pela ABNT.</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/09/reuniao-anpd.jpg" width="450" height="auto" alt="Reunião com Membros da ANPD" class=" img-fluid">	
                            <figcaption></figcaption>
                        </figure>

                        <p class="article">A FPS tem participado ativamente das consultas públicas abertas pela ANPD,
                        buscando contribuir para uma regulamentação justa para o desenvolvimento de
                        um ambiente de negócios próspero e equilibrado.</p>

                        <p class="article">As sanções administrativas da LGPD estão em vigor desde 1º de agosto de
                        2021, neste contexto, a ANPD tem papel regulatório fundamental.</p>

                        <p class="article">A ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) apoia o
                        trabalho da FPS e acompanha a questão atentamente.</p>
                      
