                        <p class="article">A Comissão de Defesa da Democracia (CDD) do Senado aprovou no início de março o Projeto de Lei (PL) 5.365/2020, que tipifica como crimes a intimidação violenta e o domínio de cidades, além de incluir este no rol de crimes hediondos. O alvo do texto são grupos armados que se organizam para praticar atos criminosos de grandes proporções, geralmente violentos e com objetivos variados, como grandes roubos, libertação de presos, provocação de terror generalizado, entre outros. </p>

                        <p class="article">O projeto adiciona os dois crimes ao Código Penal (Decreto-Lei 2.848, de 1940), além de modificar a Lei de Crimes Hediondos (Lei 8.072, de 1990), para incluir o domínio de cidades. O delito, praticado em centros urbanos de médias e grandes cidades, costuma envolver o bloqueio de rodovias e a obstrução das forças de segurança pública, com uso até mesmo de armas de uso dessas forças para impedir ou retardar a atuação do poder público. A pena prevista no projeto é de 15 a 30 anos de reclusão e pode ser aumentada em um terço, dependendo dos agravantes.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2024/05/plenario.png" width="450" height="auto" alt="Plenário do Senado" class=" img-fluid">	
                                <figcaption>Marcos Oliveira/Agência Senado</figcaption>
                        </figure>

                        <p class="article">O projeto ainda aguarda a análise e votação nas Comissões de Segurança Pública (CSP) e de Constituição, Justiça e Cidadania (CCJ).</p>

                        <p class="article">Importante lembrar que o PL 5.365/2020 incorporou o PL 3242/2021, apresentado pelo deputado federal Luiz Philippe de Orleans e Bragança (PL-SP) em 2021, que também tinha como objetivo atualizar a legislação para tornar hediondos ataques com explosivos contra empresas de transporte de valores e carros-fortes. Esse projeto foi uma sugestão da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</p>

                        <p class="article">Thiago Peixoto, Diretor Executivo da ANSEGTV, afirma que a revisão da lei é uma das principais bandeiras da entidade. “A legislação precisa ser atualizada. Os criminosos estão cada vez mais organizados e armados. É preciso coibir essas práticas. Além dos altos custos para as empresas de segurança, são vidas de trabalhadores e da população em geral que estão em risco”, diz.</p>



                        



                       

