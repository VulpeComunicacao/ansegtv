                            <p class="article">Membros da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) estiveram novamente em Brasília, no dia 5 de julho, para se reunir com integrantes do Ministério da Justiça. </p>

                            <p class="article">Desta vez, a entidade apresentou dados e informações sobre os ataques ocorridos no setor de transporte de valores. O Diretor Jurídico da ANSEGTV, Max Madruga, apresentou a perspectiva das empresas de segurança privada e alguns prognósticos da evolução dos crimes do chamado “Novo Cangaço” no país. Nesta modalidade de delito, grupos fortemente armados costumam invadir uma cidade, geralmente no interior dos estados, e atacam de forma bastante violenta instituições financeiras e bases de transportadoras de valores.</p>

                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2022/07/new_membros-ansegtv.png" width="450" height="auto" alt="Membros da ANSEGTV reunidos com Governo Federal" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>

                            <p class="article">Pela ANSEGTV, além de Max, participaram da reunião o Diretor de Relações Governamentais da entidade, Lincoln Telhado, e o advogado Mauricio Wanderley.</p>

                            <p class="article">Os representantes do Ministério da Justiça - o assessor especial do ministro Rômulo Teixeira Cavalcante e o assessor da secretaria executiva Frederico Dias - agradeceram a apresentação, destacando que a colaboração das forças de segurança pública com as empresas de segurança privada é muito importante.</p>

                            <p class="article">“Os crimes contra o setor preocupam. É importantíssimo esse suporte das autoridades. As afiliadas da ANSEGTV seguem se aperfeiçoando e aprimorando seus sistemas de defesa. Mas o apoio dos órgãos federais, sem dúvidas, é fundamental para garantir maior segurança a todos”, disse Max.</p>



                            
