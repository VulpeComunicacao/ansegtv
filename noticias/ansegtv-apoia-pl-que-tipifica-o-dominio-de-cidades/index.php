                                <span class="mx-2 author"><i class="fa fa-user"></i> ANSEGTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>09/05/2024 - 12h42</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
