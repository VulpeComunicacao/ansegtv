                        <p class="article">O Diário Oficial da União publicou no dia 19 de outubro o extrato do Acordo de Cooperação Técnica firmado entre a ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) e a PGFN (Procuradoria-Geral da Fazenda Nacional), que possibilita a todos os filiados da entidade o acesso facilitado ao refinanciamento de dívidas fiscais do PERSE (Programa Emergencial de Retomada do Setor de Eventos).</p>
 
                        <p class="article">Os interessados deverão acessar o portal Regularize <a href="https://www.regularize.pgfn.gov.br/" target="_blank" rel="noopener noreferrer"> (https://www.regularize.pgfn.gov.br/) </a>e fazer a adesão eletrônica mediante uso de senha individual.</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/10/logo-pgfn.png" width="450" height="auto" alt="Logo PGFN" class=" img-fluid">	
                            <figcaption></figcaption>
                        </figure>

                        <p class="article">O acordo tem validade de um ano, com eficácia válida já a partir de sua publicação.</p>
                        
                        <p class="article">Inicialmente, a Lei 14.148, de 3 de maio de 2021, que criou o PERSE, reunia ações emergenciais e temporárias destinadas ao setor de eventos para compensar os efeitos decorrentes das medidas de combate à pandemia da Covid-19. Posteriormente,  a Portaria 7163/2021, do Ministério da Economia, estendeu os benefícios às empresas sob o CNAE n. 8011-1/01 – atividades de vigilância e segurança privada. </p>
                        
                        <p class="article">Dentre outras vantagens, o PERSE permite a renegociação de dívidas tributárias e não tributárias, incluídas as com o FGTS; descontos de até 70% sobre valor total da dívida, com prazo máximo para quitação de até 145 meses (respeitado o § 11 do artigo 145 da Constituição Federal); além de não necessitar de pagamento de entrada mínima como condição à adesão nem apresentação de garantias reais e fidejussórias.</p>
                        
                        <p class="article">A ANSEGTV considera o acordo um grande avanço para o setor e seguirá em busca de mais medidas que contribuam para o processo de retomada econômica.</p>


