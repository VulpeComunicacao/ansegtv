                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">A ANAC (Agência Nacional de Aviação Civil) abriu recentemente Consulta Pública que propõe a revisão da Resolução nº 461/2018, que dispõe sobre os procedimentos de embarque e desembarque de passageiros armados, despacho de armas de fogo e de munição e transporte de passageiros sob custódia a bordo de aeronaves civis. A revisão ocorre devido à necessidade da regulação do embarque do vigilante armado nas operações com aeronaves fretadas e aeronaves particulares das empresas de transporte de valores, como forma de se ter maior segurança nos momentos de taxiamento e próximo ao pouso ou decolagem, situações em que realmente se observa serem mais comuns os ataques de grupos criminosos.</p>

                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/03/fachada-anac.jpg" width="450" height="auto" alt="Fachada da ANAC" class=" img-fluid">	
                                <figcaption>Ana Volpe/Jornal do Senado</figcaption>
                            </figure>
                            <p class="article">De acordo com a nova regulamentação, o embarque do vigilante armado será autorizado em parâmetros similares aos dos agentes públicos durante operações aéreas dos órgãos de polícia, bombeiro, Defesa Civil, fiscalização e congêneres.</p>
                            <p class="article">Infelizmente, são vários os casos de invasões a terminais de carga e assaltos a aviões transportadores de dinheiro. Além do roubo propriamente dito, as ações também colocam em risco a vida dos funcionários e de todos que circulam no local.</p>
                            <p class="article">Dado esse contexto, a ANSEGTV sugeriu à ANAC que, no dispositivo apresentado à consulta, art. 53-A (futuramente acrescentado à Resolução 461/2018), seja acrescentada autorização de utilização de armamento do tipo longo autorizado pela Polícia Federal pelos vigilantes na atividade de transporte aéreo de valores, em razão do tipo de ataque contra essa modalidade de transporte, na Área Restrita de Segurança.</p>
                            <p class="article">A ANSEGTV também propõe que esteja expressamente previsto na norma a necessidade de que todas as operações de transporte e custódia de valores sejam feitas com autorização prévia da Polícia Federal, conforme portaria 3.233/2012 – DG/DPF.</p>
                            <p class="article">Dessa maneira, a associação entende que será possível garantir uma maior segurança a todos, assim como uma maior excelência na qualidade do serviço prestado pelo transporte de valores.</p>
