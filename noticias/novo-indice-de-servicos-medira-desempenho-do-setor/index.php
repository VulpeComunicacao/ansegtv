                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 04/06/2021 - 13h55</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
