                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">O governo federal sancionou no dia 1º de abril a nova Lei de Licitações e Contratos Administrativos (Lei 14.133/21) que estabelece normas gerais de licitação e contratação para as Administrações Públicas diretas, autárquicas e fundacionais da União, dos Estados, do Distrito Federal e dos municípios.</p>
                            <p class="article">A nova lei tem impacto moderado para o setor de transporte de valores porque abrange parcialmente os bancos públicos. Estes respondem por expressiva parcela dos tomadores de serviços de segurança privada e transporte de valores em matéria de licitação, que continuarão a ser regidos pelo estatuto próprio (Lei nº 13.303/2016).</p>

                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/05/plenario-senado.jpg" width="450" height="auto" alt="Plenario do Senado Federal" class=" img-fluid">	
                                <figcaption>Marcos Oliveira /Agência Senado</figcaption>
                            </figure>
                            <p class="article">As novas regras coexistirão por dois anos com as da Lei 8.666/1993 (antiga Lei Geral), da Lei 10.520/2002 (Lei do Pregão) e da Lei 12.462/2011 (RDC), exceto quanto aos crimes licitatórios, que foram revogadas desde já. Assim, será opcional utilizar a nova ou a antiga legislação até 1º de abril de 2023.</p>

                            <p class="article">Os crimes licitatórios, antes todos punidos com mera detenção, sofreram de modo geral majoração de pena e, à exceção dos crimes de “perturbação de licitação” (art.337-I) e “violação de sigilo” (art. 337-J), passaram a ser puníveis com pena de reclusão, permitindo, dentre outros aspectos, a autorização de interceptação telefônica e o regime inicial fechado, dentre outros aspectos processuais e de execução penal.</p>

                            <p class="article">Um dos destaques da nova legislação é o item V do artigo 14, segundo o qual não poderão licitar ou executar contrato, direta ou indiretamente, empresas controladoras, controladas ou coligadas, concorrendo entre si. A medida tem como objetivo prevenir conluios fraudulentos.</p>
                            <p class="article">A ANSEGTV, como representante do setor de transporte de valores, acredita que a nova legislação é positiva ao unificar as diversas normas e proporcionar maior transparência.</p>
