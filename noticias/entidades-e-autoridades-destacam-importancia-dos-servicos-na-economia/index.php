                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">

                        <p class="article">Em webinar nesta terça-feira (8), a Frente Parlamentar do Setor de Serviços (FPS) lançou o Índice de Serviços (ISe), que vai medir mensalmente o desempenho das atividades do setor e identificar sua capacidade de contribuir para a promoção do desenvolvimento econômico sustentado e inclusivo.</p>

                        <p class="article">O novo indicador utilizará dados de variação da receita nominal e do volume de serviços, e os dados do CAGED (Cadastro Geral de Empregados e Desempregados), que é composto pelas informações relacionadas ao mercado de trabalho formal.</p>

                        <p class="article">Presidente da FPS, o deputado federal Laércio Oliveira destacou a importância do setor para a economia, na geração de empregos, de renda e no desenvolvimento do país em geral.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/06/meet-lancamento-ise-04-06.png" width="450" height="auto" alt="Meet - Lançamento da ISe" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>
                         
                        <p class="article">Dentre outras autoridades presentes, o secretário de Desenvolvimento da Indústria, Comércio, Serviços e Inovação, Jorge Luiz de Lima, defendeu a realização de reformas estruturantes e a diminuição do Custo Brasil como medidas essenciais para o crescimento econômico.</p>

                        <p class="article">Rafael Sampaio, chefe de gabinete da Secretaria de Governo da Presidência da República, pasta ocupada pela ministra Flávia Arruda, ressaltou medidas lançadas pelo governo federal durante a pandemia, como o Pronampe (Programa Nacional de Apoio às Microempresas e Empresas de Pequeno Porte), e disse que novas ações são estudadas para auxiliar o setor.</p>

                        <p class="article">O presidente da ANSEGTV, Gabriel Damasceno, enalteceu o lançamento do ISe. “O novo índice leva a Frente a outro patamar, propiciando maior representatividade ao setor, além de possibilitar um maior diálogo com os diversos agentes econômicos”, disse. </p>

                        <p class="article">Damasceno lembrou ainda que a atividade de transporte de valores foi considerada essencial desde o início da pandemia, não parando em momento nenhum, o que foi fundamental, por exemplo, para o pagamento do auxílio-emergencial.</p>

                        <p class="article">Uma das apoiadoras da FPS, a ANSEGTV também foi representada por Milena Oliveira, Relações Institucionais da entidade.</p>

                        <p class="article">O encerramento do evento contou com a fala do ministro da Economia, Paulo Guedes. Ele elogiou a atuação do deputado Laércio, valorizou o trabalho da Frente, destacou programas do governo e apresentou números que mostram um processo de recuperação econômica.</p>

                        <p class="article">Guedes ainda anunciou a extensão do pagamento do auxílio emergencial por mais dois ou três meses e defendeu uma Reforma Tributária justa, com simplificação de alíquotas, sem prejudicar o setor de serviços.</p>

