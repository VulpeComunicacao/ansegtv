                            <h1>Câmara aprova projeto para combater “novo cangaço”</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>09/09/2022 - 11h55</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                        
                        <p class="article">A Câmara dos Deputados aprovou no início de agosto um substitutivo do deputado Neucimar Fraga (PP-ES) ao Projeto de Lei 5365/20, do deputado Sanderson (PL-RS), que cria medidas para combater e punir o chamado "novo cangaço", quando grupos criminosos fortemente armados exercem domínio sobre as cidades e impedem a reação das forças de segurança local. A proposta, que tipifica e torna hediondo o crime de domínio de cidades e ainda cria o tipo penal de intimidação violenta, aguarda agora a análise do Senado.</p>

                        <p class="article">O projeto define como domínio de cidade "realizar bloqueio total ou parcial de quaisquer vias de tráfego, terrestre ou aquaviário, bem como de estruturas físicas das forças de segurança pública, para evitar e/ou retardar a aproximação do poder público, com emprego de armas de fogo e/ou equipamentos de uso das forças de segurança pública, com finalidade de praticar crimes".</p>

                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2022/09/novo-cangaco.png" width="450" height="auto" alt="Camara aprova projeto para combater o novo cangaco" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>

                        <p class="article">Já o crime de intimidação violenta consiste em realizar ou promover ato de incêndio, depredação, saque, destruição ou explosão, contra bens públicos ou privados, de acesso ou destinados aos serviços públicos, de forma a impedir ou embaraçar a atuação do poder público voltada para a prevenção ou repressão de crimes, a realização da execução penal ou a administração do sistema penitenciário.</p>

                        <p class="article">Segundo o texto, o crime de domínio de cidades será punido com reclusão de 15 a 30 anos, mas poderá ser de 20 a 30 anos e multa, se a violência resultar em lesão corporal grave; e 20 a 40 anos e multa, se houver morte. Ainda há agravantes previstos com acréscimo de 1/3 da pena se a ação, por exemplo, utilizar dispositivos explosivos e/ou captura de reféns. Já a pena para intimidação violenta será a de reclusão de 6 a 12 anos, também com aumento de 1/3 se a ação for decorrente, por exemplo,  de ordem, orientação ou tenha como motivação causa relacionada a preso, provisório ou condenado, ou a líder ou membro de facção criminosa.</p>

                        <p class="article">O presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno, concorda com as propostas aprovadas. “As ações criminosas estão cada vez mais sofisticadas e violentas. Além do prejuízo financeiro, são inúmeras vidas colocadas em risco. É preciso atualizar a legislação como forma de inibir essas condutas”.</p>
                            
