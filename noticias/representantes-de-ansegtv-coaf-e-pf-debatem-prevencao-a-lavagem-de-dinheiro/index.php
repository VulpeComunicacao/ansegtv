                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">

                        <p class="article">O Diretor Jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Max Tavares Madruga, a Delegada Chefe da DICOF/CGCSP/DIREX/PF, Dra. Denise Vargas Tenorio, e a Coordenadora-Geral de Fiscalização e Regulação do COAF, Flávia Maria Valente Carneiro, participaram na última quinta-feira (26) da live “ANSEGTV na Prevenção à Lavagem de Dinheiro”. </p>

                        <p class="article">O encontro, realizado na sede da entidade, em Brasília, foi transmitido a todos os associados. Ao menos 400 pessoas acompanharam o evento.</p>

                        <p class="article">Os principais temas discutidos foram relacionados à Lei 9.613/98, que dispõe sobre os crimes de "lavagem" ou ocultação de bens, direitos e valores, e a IN PF 196/21, que normatiza o procedimento de comunicação de operações suspeitas ou que contenham indícios de crimes de lavagem de dinheiro por empresas de transporte de valores.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2023/10/representantes.png" width="450" height="auto" alt="Representantes da ANSEGTV, COAF e PF" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">Dentre outros pontos, Flávia Carneiro destacou que a lavagem de dinheiro é um crime de difícil configuração, o que exige um esforço conjunto de transportadoras, seguradoras e autoridades. Lembrou ainda que não é papel das empresas do setor investigar, mas sim identificar e comunicar possíveis indícios da prática. “A qualidade dessa comunicação avançou. É um setor que demonstra maturidade nesse tema, mas sempre é possível melhorar e capacitar”, disse. </p>

                        <p class="article">Flávia também ressaltou que não só o transporte de numerário exige atenção, mas também o de bens de alto valor agregado, metais preciosos, dentre outros.</p>

                        <p class="article">Já a Dra. Denise Vargas Tenorio disse ser favorável a uma atualização da IN PF 196/21. Também defendeu que toda empresa do setor procure conhecer a fundo o contratante na hora de fechar uma operação.</p>

                        <p class="article">Afirmou ainda que é preciso ter uma política clara para identificar indícios de lavagem de dinheiro. “O funcionário precisa saber quem buscar, a quem denunciar e como proceder”, comentou.</p>

                        <p class="article">Max agradeceu a presença das duas convidadas, se colocando à disposição da PF e do COAF para colher juntos aos associados sugestões para melhorias no setor de segurança e transporte de valores. As participantes disseram que também estão abertas a ouvir todas as dúvidas e ideias.</p>


