                            <h1>Estatuto do Aprendiz é necessário, mas mantém trechos preocupantes</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>19/12/2022 - 12h</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
