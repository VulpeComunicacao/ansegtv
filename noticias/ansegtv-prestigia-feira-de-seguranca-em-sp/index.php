                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                        
                        <p class="article">A ISC Brasil, uma das principais feiras de soluções integradas de segurança do Brasil, reuniu, de 21 a 23 de setembro, no Expo Center Norte, em São Paulo, mais de 20 mil profissionais, entre gestores de segurança, usuários finais, consultores, integradores de tecnologia e autoridades. </p>

                        <p class="article">O evento abrangeu todo o ecossistema de segurança integrada, desde câmeras de videomonitoramento, vigilância por vídeo, controle de acesso, biometria, portaria remota, drones, inteligência artificial, cibersegurança, dentre outros serviços.</p>

                        <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2022/09/new-equipe-ansegtv.png" width="450" height="auto" alt="Equipe ANSGETV em Feira de Segurança" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>

                        <p class="article">A diretoria da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) esteve no evento. Representaram a entidade: Fábio Oliveira, Diretor Regional Norte e Nordeste; Fátima Dias, Diretora de Relações Institucionais; Max Madruga, Diretor Jurídico; Rafael Alcântara, Diretor de Tecnologia e Inovação; e Robson Barbara, Diretor Social e de Comunicação.</p>

                        <p class="article">A Comando G8, afiliada da ANSEGTV, contou com um grande estande na feira, onde apresentou seus veículos e serviços.</p>

                        <p class="article">Também durante o evento, a secretária de Segurança Urbana de São Paulo, Elza Paulina de Souza, explicou como funcionará a nova plataforma de videomonitoramento, Smart Sampa. Estão previstas as instalações de 20 mil câmeras na cidade até 2024, com um investimento estimado em R$ 70 milhões por ano.</p>

                        <p class="article">Para Robson Barbara, Diretor Social e de Comunicação da ANSEGTV, a feira, além de apresentar as últimas novidades em sistemas e equipamentos, foi importante pela troca de experiências. “A ISC Brasil é uma referência no calendário de eventos da área. Poder discutir questões do setor com especialistas é sempre engrandecedor”, considerou.</p>
                        


                            
