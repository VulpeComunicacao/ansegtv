                        <p class="article">A visita institucional teve como objetivo apresentar a ANSEGTV, além de discutir temas como os crimes contra guardas de valores, principalmente o domínio de cidades e os ataques contra carros-fortes em estradas, com uso de explosivos. Atualmente, tramita no Senado o Projeto de Lei 5365/2020 que tipifica o “domínio de cidades” e a “intimidação violenta” como crimes no Código Penal. Além disso, a proposta inclui o primeiro no rol de crimes hediondos.  </p>

                        <p class="article">“Foi uma reunião bastante produtiva em que pudemos apresentar nossa visão sobre o setor de transporte e guarda de valores. Tivemos a oportunidade ainda de tratar sobre a importância do Plano de Defesa para as cidades que possuem instalações custodiantes de numerário, com a prática de simulação do domínio de cidades, envolvendo todos os órgãos de Segurança Pública e demais entidades da sociedade, com o objetivo de preparar as forças de segurança para as situações de crise. Fomos muito bem recebidos e a SENASP ficou de dar andamento aos temas", disse Max.</p>

