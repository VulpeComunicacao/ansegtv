                            <p class="article">O deputado Aguinaldo Ribeiro (PP-PB) apresentou no dia 12 seu relatório final à Comissão Mista da Reforma Tributária. O texto propõe mudanças significativas ao sistema tributário brasileiro. A principal alteração é a substituição de tributos como PIS, COFINS, IPI, ICMS e ISS pelo Imposto sobre Bens e Serviços (IBS).</p>

                            <p class="article">Para embasar o texto, o parlamentar aproveitou pontos de três matérias: a proposta de emenda à Constituição (PEC) 45/2019, originária na Câmara; a PEC 110/2019, apresentada no Senado; e o PL 3.887/2020. Enquanto as PECs convergem para a extinção de tributos incidentes sobre bens e serviços, o projeto de lei, de autoria do Poder Executivo, trata da instituição da Contribuição Social sobre Operações com Bens e Serviços (CBS).</p>

                            <p class="article">A proposta que cria o IBS visa solucionar a competência de cada Estado para tributar e legislar sobre sua fração, o que, segundo o relator, gerou uma infinidade de regras distintas, que tornaram o atual sistema ineficaz e contrário à harmonização federativa.</p>

                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/05/reforma-tributaria.jpg" width="450" height="auto" alt="Reforma Tributária" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            
                            <p class="article">Ainda segundo o relatório, o IBS sobre cada operação seria calculado pelo somatório de três alíquotas: da União, dos estados ou Distrito Federal e dos municípios. Cada um desses governos, então, deverá exercer individualmente sua autonomia tributária com fixação da sua respectiva alíquota a ser definida em lei ordinária, sendo que as demais normas referentes ao imposto serão normatizadas em lei complementar, com execução obrigatória para todos.</p>

                            <p class="article">No substitutivo, o relator prevê a substituição do IPI por um Imposto Seletivo, para complementar o IBS, ao incidir em uma ou mais fases da cadeia produtiva, a ser determinada pelo legislador. Pelo texto, a devolução do IBS será feita com base no consumo estimado das famílias.</p>

                            <p class="article">Preocupa o setor de serviços o fato do IBS não ser cumulativo, deixando poucas opções de créditos a compensar. O ramo da economia que mais emprega no país funciona basicamente com mão de obra. Assim, a desoneração da folha de pagamento segue urgente e necessária. Da forma apresentada, o IBS onera por demais o setor, criando um desequilíbrio tributário entre os grupos econômicos.</p>

                            <p class="article">O Brasil possui um sistema tributário muito oneroso e complexo, o que gera grande litigiosidade e gastos. Fatiada ou mais ampla, o país precisa de uma reforma justa e equilibrada, sem aumento de carga tributária, seja geral ou setorial.</p>
                        
