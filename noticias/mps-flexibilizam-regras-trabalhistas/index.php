                        <p class="article">A Câmara dos Deputados aprovou no dia 12 de agosto a Medida Provisória 1045/2021, que renova o programa de redução ou suspensão de salários e jornada de trabalho com o pagamento de um benefício emergencial aos trabalhadores. A MP segue agora para análise do Senado. </p>

                        <p class="article">No âmbito das relações trabalhistas, dentre outras novidades, a medida institui o Regime Especial de Trabalho Incentivado, Qualificação e Inclusão Produtiva (Requip), o Programa Primeira Oportunidade e Reinserção no Emprego (Priore), e o Programa Nacional de Prestação de Serviço Social Voluntário.</p>

                        <p class="article">O Requip, destinado a quem está sem registro em carteira de trabalho há mais de dois anos, a jovens de 18 a 29 anos e beneficiários do Bolsa Família com renda mensal familiar de até dois salários mínimos, surge como um potencial substituto para o programa Jovem Aprendiz. Ele prevê o pagamento de bônus pelo trabalho em jornadas semanais de até 22 horas (BIP) e de uma bolsa por participação em cursos de qualificação de 180 horas ao ano (BIQ).</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/08/carteira-trabalho.jfif" width="450" height="auto" alt="Carteira de Trabalho" class=" img-fluid">	
                            <figcaption>Arquivo/Agência Brasil</figcaption>
                        </figure>

                        <p class="article">O Priore é direcionado a jovens entre 18 e 29 anos, no caso de primeiro emprego com registro em carteira, e a pessoas com mais de 55 anos sem vínculo formal há mais de 12 meses. Ele é semelhante ao Carteira Verde e Amarela, proposto com a MP 905/20. </p>

                        <p class="article">Já o Programa Nacional de Prestação de Serviço Social Voluntário também visa jovens de 18 a 29 anos e a pessoas com mais de 50 anos, com duração de 18 meses em atividades de interesse público dos municípios.Da mesma forma que os outros programas previstos na MP, o selecionado deverá realizar curso de qualificação profissional. O monitoramento do programa será feito pelas cidades de forma informatizada. </p>
                        
                        <p class="article">A MP prevê ainda a redução do valor de horas extras a categorias que atualmente têm jornadas reduzidas previstas na legislação e menores que as oito horas diárias tradicionais da CLT. O projeto prevê uma "extensão da jornada" até às oito horas e determina que o pagamento de cada uma dessas horas extras tenha acréscimo de 20% - atualmente, a legislação trabalhista determina acréscimo de 50% (por hora trabalhada de segunda a sábado) e 100% (por hora trabalhada aos domingos e feriados).</p>

                        <p class="article">Outra MP em discussão atualmente na Câmara é a 1046, que também flexibiliza a legislação trabalhista, com regras que poderão ser adotadas pelos empregadores por quatro meses.</p>

                        <p class="article">O texto, semelhante ao da MP 927/20, permite que o empregador altere o regime de trabalho presencial para o teletrabalho e também antecipe as férias do funcionário. Outro ponto é a possibilidade do patrão suspender o pagamento do FGTS (Fundo de Garantia do Tempo de Serviço) por 120 dias.</p>

                      
