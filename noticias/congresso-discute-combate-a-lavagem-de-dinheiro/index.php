                        <p class="article">O IPLD (Instituto de Prevenção e Combate à Lavagem de Dinheiro e ao Financiamento ao Terrorismo) realiza nos dias 23 e 24 de maio a 5º edição de seu Congresso Internacional, o maior da América Latina na área. A ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) é apoiadora do evento.</p>

                        <p class="article">Segundo a organização, o objetivo do encontro é promover conhecimentos e integração entre o mercado, especialistas e profissionais estratégicos que atuam com integridade corporativa.</p>

                        <p class="article">Dentre os participantes, diversos especialistas e autoridades, como David Luna, presidente do Grupo de Especialistas em Comércio Anti-Ilícito da OCDE (Organização para a Cooperação e Desenvolvimento Econômico); Vladimir Aras, procurador-regional da República em Brasília; e Guilherme France, gerente do Centro de Conhecimento Anticorrupção (CCA) da Transparência Internacional – Brasil, e consultor na Rede Brasil do Pacto Global das Nações Unidas.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2023/03/congresso-ipdl.png" width="450" height="auto" alt="Congresso IPDL" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">A prevenção à lavagem de dinheiro e o combate ao financiamento do terrorismo são temas importantes para o setor de transporte de valores. No Brasil, o COAF (Conselho de Controle de Atividades Financeiras) lidera as medidas e regulações relativas à área.</p>

                        <p class="article">Para disfarçar os lucros ilícitos sem comprometer os envolvidos, a lavagem de dinheiro consiste em três processos: primeiro, o distanciamento dos fundos de sua origem, evitando uma associação direta deles com o crime; segundo, o disfarce de suas várias movimentações para dificultar o rastreamento desses recursos; e terceiro, a disponibilização do dinheiro novamente para os criminosos depois de ter sido suficientemente movimentado no ciclo de lavagem e poder ser considerado "limpo".</p>

                        <p class="article">Já a luta contra o financiamento do terrorismo está intimamente ligada ao combate à lavagem de dinheiro. Grandes atentados levaram os países a intensificar a cooperação mútua na área.</p>
                        
                        <p class="article">As inscrições para o evento podem ser feitas pelo site: <a href=" https://congresso5.ipld.com.br/#Inscreva" target="_blank"> https://congresso5.ipld.com.br/#Inscreva</a></p>
                            
