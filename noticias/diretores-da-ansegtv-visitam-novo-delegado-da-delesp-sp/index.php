                        <p class="article">As unidades regionais da DELESP são subordinadas à Coordenação-Geral de Controle de Serviços e Produtos
(CGCSP) e às Superintendências de Polícia Federal nos Estados e no Distrito Federal. Dentre suas funções, está o combate à prática irregular da atividade de segurança privada e as ações de prevenção à Lavagem de Dinheiro - PLD.</p>

                        

                        <p class="article">“A ANSEGTV defende o exercício da segurança privada, sempre respeitando todas as normas legais. Consideramos a reunião extremamente positiva. O diálogo com as autoridades é vital”, diz Max Madruga.</p>

                        

                       

