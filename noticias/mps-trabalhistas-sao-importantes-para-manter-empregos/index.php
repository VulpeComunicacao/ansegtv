                            <h1>MPs trabalhistas são importantes para manter empregos</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 05/05/2021 - 14h55</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
