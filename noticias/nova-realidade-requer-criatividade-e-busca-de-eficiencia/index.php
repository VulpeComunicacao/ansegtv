                            <p class="article"><b>Pergunta: O chamado “Novo Cangaço” segue causando preocupação. Quais os principais problemas que esse tipo de crime causa à população?</b></p>

                            <p class="article">O “novo cangaço”, que atualmente tem sido chamado de “domínio de cidades”, é um grande mal para toda a sociedade! Além do terror para a população da cidade atacada, traz graves consequências, como por exemplo a falta de acesso ao numerário no município, a destruição do patrimônio público e privado e o financiamento de grupos criminosos, o que acaba permitindo a incidência de novos crimes (o dinheiro roubado normalmente é utilizado para a aquisição de mais armas e drogas). A pior consequência é quando os ataques acarretam mortes de policiais e da população. </p>
                            
                            
                            <p class="article"><b>Pergunta: Qual seria a melhor forma de combater esses ataques?</b></p>
                            <p class="article">O combate a esse tipo de crime tem que se intensificar, pois os valores roubados servem de fonte de receita para o crime organizado. Uma das iniciativas em andamento no Congresso Nacional é um Projeto de Lei que visa tipificar esse crime como hediondo, o que aumenta a pena dos criminosos envolvidos nas ocorrências. Mas é preciso muito mais, como a disponibilização de investimentos no treinamento dos policiais para enfrentamento a essa modalidade de crime e o aparelhamento das unidades policiais dos municípios que potencialmente podem ser alvos desse tipo de ataque.</p>
       

                            <p class="article"><b>Pergunta: A ANSEGTV e seus associados estão sempre em contato com autoridades de segurança e participando de seminários. Pretendem seguir com esse movimento neste ano?</b></p>
                            <p class="article">Sem dúvida, a troca de informações e experiências é super importante. Seja com autoridades ou em eventos da área, a busca por novos conhecimentos e aprendizados ajuda muito em nossa atividade. A todo momento, ocorrem mudanças tecnológicas, normativas. É preciso se atualizar constantemente e a ANSEGTV estará sempre aberta ao diálogo com toda sociedade.</p>


                            <p class="article"><b>Pergunta: Muito se fala sobre o crescimento do uso de meios digitais de pagamento. Como o sr. avalia o atual cenário?</b></p>
                            <p class="article">Algo que sempre defendo é o convívio harmonioso entre todos os meios de pagamento. São todos complementares. O dinheiro físico ainda tem um papel muito importante, principalmente no comércio e entre a população de baixa renda. Mas, claro, não é possível ignorar a chegada das formas digitais de transferência de valores. Por esse motivo, entendo que os players da nossa área devem buscar novas formas de atuação, investimento em novos segmentos, serviços e produtos. Isso, sempre tendo como objetivo a eficiência, aliada ao menor custo possível e à satisfação do cliente. Já há empresas focando em atividades que antes não exerciam, como o transporte de cargas de alto valor (por exemplo, remédios e eletrônicos), cofres inteligentes desenvolvidos e produzidos de acordo com as características de cada cliente. Essa tendência é positiva.</p>


                            <p class="article"><b>Pergunta: Por último, que mensagem o sr. gostaria de deixar aos associados para esse ano?</b></p>
                            <p class="article">Sem dúvidas, é mais um ano desafiador. Como disse anteriormente, a dinâmica operacional, especialmente em nosso ramo, vem se modificando de forma rápida. É preciso procurar alternativas e caminhos para que continuemos atuando, gerando empregos e negócios. Então, essa é a mensagem principal. Não podemos desanimar, nem esmorecer. Foco no cliente, melhoria de processos, novos produtos. Com criatividade e força de vontade, conseguiremos seguir atuando de forma competente e eficiente.</p>



