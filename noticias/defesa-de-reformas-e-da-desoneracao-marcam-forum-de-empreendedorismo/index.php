                        <p class="article">O Instituto Unidos Brasil (IUB) realizou nesta terça-feira (9), em Brasília, o Fórum Desafios do Empreendedorismo e Geração de Empregos, que contou com a participação de vários empresários, dos mais variados setores, e políticos.</p>

 

                        <p class="article">O evento foi acompanhado, de forma remota, pelo presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno.</p>

                        

                        <p class="article">O seminário foi marcado pela defesa das reformas Tributária, Administrativa, além da desoneração da folha de pagamentos.</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/11/forum-defesa.png" width="450" height="auto" alt="Forum" class=" img-fluid">	
                            <figcaption> Divulgação/IUB</figcaption>
                        </figure>

                        <p class="article">O presidente do IUB, Nabil Sahyoun, lamentou os impactos da pandemia na economia, como o desemprego e a perda de poder aquisitivo da população. Ele também pediu que o governo adote medidas efetivas para a retomada do crescimento.</p>

                        

                        <p class="article">Professora da FGV, Lígia Sica disse que o desemprego levou ao aumento do empreendedorismo, mas muitas vezes sem a melhor qualificação ou preparo, muito mais por uma necessidade de sobrevivência.</p>

                        

                        <p class="article">Tirso Meirelles, presidente do Sebrae-SP, destacou que as micro e pequenas empresas são responsáveis por 30% PIB e 50% das vagas de emprego no país.</p>

                        

                        <p class="article">Miguel Torres, presidente da Força Sindical, afirmou que empresários e trabalhadores possuem o mesmo objetivo: um país mais justo e com mais emprego. Assim como outros debatedores, também defendeu uma Reforma Tributária ampla.</p>

                        

                        <p class="article">O encerramento ficou por conta do senador Antonio Anastasia (PSD-MG). Ele afirmou que o Poder Público deve fomentar um ambiente propício para que a iniciativa privada consiga gerar mais vagas de trabalho. Também ressaltou a importância do planejamento e da criação de uma agenda de prioridades pelo Congresso.</p>

