                        <p class="article">O Diretor Jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Max Tavares Madruga, e o Diretor de Assuntos Associativos e Sindicais da entidade, José Ferreira de Andrade Netto, participaram, na quarta-feira (20), da abertura do 10º ENEFAV - Encontro Nacional das Escolas de Formação e Aperfeiçoamento de Vigilantes.</p>

                        <p class="article">O evento, organizado pela ABCFAV (Associação Brasileira dos Cursos de Formação e Aperfeiçoamento de Vigilantes) e encerrado na sexta (22), foi realizado no Novotel São Paulo Center Norte, na capital paulista.</p>

