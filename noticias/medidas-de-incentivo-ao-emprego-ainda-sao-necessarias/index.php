                            <h1>Medidas de incentivo ao emprego ainda são necessárias</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i> 12/03/2021 - 13h55</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
                            </p>
                            <hr class="divider">
                        </div>
                    </div>
                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">No início de 2020, no começo da pandemia, o governo federal anunciou uma série de medidas que permitiram a manutenção de milhões de empregos. À época, todos acreditavam que, um ano depois, a situação estaria estabilizada.<br>Infelizmente, não foi o que ocorreu. Recordes de casos e de mortes pela Covid-19 são batidos diariamente. Com hospitais sobrecarregados, governos restringem atividades e a circulação de pessoas.</p>

                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/03/min-economia.jpg" width="450" height="auto" alt="Fachada do Ministério da Economia" class=" img-fluid">	
                                <figcaption>Marcello Casal Jr./Agência Brasil</figcaption>
                            </figure>
                            <p class="article">Serviço essencial, o transporte de valores não para, atuando para que a população não fique desabastecida. No entanto, empresas seguem com dificuldades financeiras e o cenário econômico permanece oscilante.</p>
                            <p class="article">Nos últimos dias, a imprensa vem noticiando que o Ministério da Economia pretende lançar um novo pacote de ações. Essas seriam nos moldes das MPs 927 e 936, permitindo novamente a redução da jornada de trabalho, a suspensão de contratos, o parcelamento dos depósitos do FGTS, além da antecipação de férias individuais e coletivas. Os prazos de vigência de tais medidas ainda são estudados.</p>
                            <p class="article">Sobre salários, informações iniciais apontam que os parâmetros devem ser os mesmos de 2020, com possíveis reduções de 25%, 50% e 70%, através de acordos, com complementação de renda pelo governo. Entre outras ideias estudadas, estão ainda a antecipação do seguro-desemprego e a utilização de recursos do Fundo de Amparo ao Trabalhador (FAT).</p>
                            <p class="article">Ainda de acordo com a imprensa, o governo também planeja uma nova rodada do saque emergencial do FGTS. No ano passado, os trabalhadores puderam resgatar o valor de até um salário mínimo das contas ativas e inativas do fundo.</p>
                            <p class="article">No último dia 8, o governo anunciou o aumento do período de carência dos empréstimos concedidos pelo Pronampe. O programa tinha prazo de carência de oito meses, e os empréstimos começariam a ser pagos em março. Com a prorrogação, as primeiras parcelas começam a vencer em junho. Medidas como essa são de vital importância, principalmente para micro e pequenos empresários.</p>
                            <p class="article">A ANSEGTV apoia essas iniciativas e entende como essenciais para o atual momento. Apenas com união e resiliência conseguiremos atravessar esse período tão difícil.</p>
