                        <p class="article">Em evento promovido pelo IPLD (Instituto de Prevenção e Combate à Lavagem de Dinheiro e ao Financiamento do Terrorismo), nesta quinta-feira (24), o Diretor de Supervisão do COAF (Conselho de Controle de Atividades Financeiras), Rafael Bezerra Ximenes de Vasconcelos, esclareceu os principais pontos da Resolução 36 do órgão.</p>

                        <p class="article">A norma entrou em vigor recentemente e traz novas regulamentações para o âmbito de prevenção à lavagem de dinheiro, ao financiamento do terrorismo e à proliferação de armas de destruição em massa (PLD/FT).</p>
                        
                            <figure class="pull-right m-2 text-left">
                                    <img src="../../img/uploads/2021/06/webinar-coaf.jpg" width="450" height="auto" alt="Webinar - COAF" class=" img-fluid">	
                                    <figcaption></figcaption>
                            </figure>

                        <p class="article">Rafael Bezerra explicou que empresas de segmentos como joalherias, bens de luxo e alto valor, factoring e negociações de direitos de transferência de atletas ou artistas estão agora sujeitas à regulação do COAF, passando a receber novas diretrizes e obrigações em suas atividades.</p>

                        <p class="article">A nova norma complementa a regulamentação já vigente para cada um dos setores submetidos à supervisão do COAF, estabelecendo o conteúdo mínimo da política de PLD/FTP e dispondo sobre estrutura de governança, avaliação interna de riscos, contínua capacitação de funcionários sobre o tema, entre outros procedimentos.</p>

                        <p class="article">Rafael Bezerra disse ainda que a Resolução 36 está em convergência com as demais normas infralegais, de órgãos como CVM (Comissão de Valores Mobiliários) e Banco Central. Ele também considerou que, a partir de agora, as empresas não poderão mais tratar o tema - PLD/FTP - com superficialidade.</p>

                        <p class="article">A ANSEGTV acompanhou o evento, representada por Robson Alexandre Barbara, Diretor de Comunicação, e Roberto Severino da Cruz Júnior, Diretor de Relacionamento com o Cliente.</p>

 

