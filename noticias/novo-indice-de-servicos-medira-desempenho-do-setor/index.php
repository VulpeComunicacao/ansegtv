                        <p class="article">Idealizado pela Frente Parlamentar do Setor de Serviços (FPS), o Índice de Serviços (ISe) será lançado na próxima terça-feira, 8 de junho, às 10h. O indicador avaliará mensalmente a capacidade de promoção de desenvolvimento sustentado no setor que responde por mais de 60% do PIB e por 57% dos empregos formais em todo país.</p>

                        <p class="article">Para marcar seu lançamento, a FPS organizou o webinar “Setor de Serviços em Dados”, que será transmitido ao vivo em seu canal do YouTube.</p>

                        <p class="article">O ISe reunirá dados do índice de variação da receita nominal e do volume de serviços - seja por atividade, número de serviços e suas subdivisões - e do novo CAGED (Cadastro Geral de Empregados e Desempregados), que é composto pelas informações relacionadas ao mercado de trabalho formal, sob a responsabilidade da Secretaria do Trabalho do Ministério da Economia. </p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/06/lancamento-ise-04-06.jpg" width="450" height="auto" alt="Lançamento da ISe" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">Entre as autoridades convidadas para o evento estão o ministro da Economia, Paulo Guedes, o presidente da Câmara, Arthur Lira, o secretário especial de Produtividade, Emprego e Competitividade, Carlos Da Costa, o secretário de Desenvolvimento da Indústria, Comércio, Serviços e Inovação, Jorge Luiz de Lima, e a ministra da Secretaria de Governo, Flávia Arruda. </p>

                        <p class="article">Suprapartidária, a FPS foi criada em 2019 e conta com o apoio das seguintes entidades: Associação Nacional de Segurança e Transporte de Valores (ANSEGTV), Associação Nacional dos Profissionais e Empresas Promotoras de Crédito e Correspondentes no País (ANEPS), Associação Nacional de Fomento Comercial (ANFAC), Associação Brasileira de Administradoras de Consórcios (ABAC), Associação de Medicina Diagnóstica (ABRAMED), Associação de Universidades Particulares (ANUP), Associação de Marketing Promocional (AMPRO); Instituto de Gestão de Excelência Operacional em Cobrança (IGEOC), Federação Nacional dos Sindicatos de Empresas de Recursos Humanos, Trabalho Temporário e Terceirizado (FENASERHTT) e Federação Nacional de Instalação e Manutenção de Infraestrutura de Redes de Telecomunicações e de Informática (FENINFRA).</p>



