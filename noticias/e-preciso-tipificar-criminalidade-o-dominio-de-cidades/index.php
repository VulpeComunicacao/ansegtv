                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">

                        <p class="article"> No dia 9 de abril, criminosos invadiram uma empresa de transporte de valores no interior do Mato Grosso. Não tiveram sucesso, mas espalharam medo e destruição pela cidade. Em 13 de fevereiro, suspeitos de roubo a bancos foram mortos em confronto com a Polícia Militar, nas imediações de Soledade, no Agreste paraibano. Uma semana depois, a PM identificou um outro grupo que planejava um ataque a uma agência bancária em Águas Formosas (MG). Já em novembro do ano passado, integrantes de uma quadrilha responsável por diversas explosões no Rio de Janeiro planejava mais um ataque quando foi surpreendida por bandidos rivais. No confronto, mais mortes.</p>

                        <p class="article"> O noticiário é farto sobre episódios de violência que têm como objetivo a subtração de altos valores. Ultimamente, casos como os citados são classificados, de forma genérica, como “novo cangaço”, expressão criada no final dos anos 1990 para classificar ataques armados, principalmente no interior do país, geralmente em cidades pequenas. Nos últimos anos, porém, uma nova modalidade, conhecida como “domínio de cidades”, ganhou maior força. São ações mais elaboradas, com maior número de criminosos, em municípios mais populosos, que envolvem bloqueio de vias e armas mais pesadas.</p>

                        <p class="article"> Além da violência empregada e das perdas humanas, essas ações trazem graves consequências, como por exemplo a falta de acesso ao numerário no município, a destruição do patrimônio público e privado e o financiamento de grupos criminosos, o que acaba permitindo a incidência de novos delitos (o dinheiro roubado normalmente é utilizado para a aquisição de mais armas e drogas).</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2020/11/max-tavares.png" width="450" height="auto" alt="Max Tavares" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">É preciso intensificar o combate a essas ações. Uma das iniciativas em andamento no Congresso Nacional é o Projeto de Lei 5365/2020, que visa tipificar o domínio de cidades como crime hediondo, o que aumenta a pena dos envolvidos nas ocorrências.</p>

                        <p class="article">O projeto define a prática como "realizar bloqueio total ou parcial de quaisquer vias de tráfego, terrestre ou aquaviário, bem como de estruturas físicas das forças de segurança pública, para evitar e/ou retardar a aproximação do poder público, com emprego de armas de fogo e/ou equipamentos de uso das forças de segurança pública, com finalidade de praticar crimes". </p>

                        <p class="article">O PL também cria o crime de intimidação violenta, que consiste “em realizar ou promover ato de incêndio, depredação, saque, destruição ou explosão, contra bens públicos ou privados, de acesso ou destinados aos serviços públicos, de forma a impedir ou embaraçar a atuação do poder público voltada para a prevenção ou repressão de crimes, a realização da execução penal ou a administração do sistema penitenciário”. A aprovação seria um importante passo para repelir essas ações. A proposta já passou pela Câmara dos Deputados e aguarda análise do Senado. Ela tramita em conjunto com o PL 610/2022, que visa tipificar o “novo cangaço” como ato de terrorismo. As matérias vão ao exame da CSP (Comissão de Segurança Pública), seguindo posteriormente à CCJ (Comissão de Constituição e Justiça).</p>

                        <p class="article"> Obviamente, é preciso muito mais que o endurecimento da legislação, como a disponibilização de investimentos no treinamento dos policiais para enfrentamento dessa modalidade de crime e o aparelhamento das unidades de segurança dos municípios que potencialmente podem ser alvos desse tipo de ataque. A prevenção e o preparo ainda são as melhores opções de combate a essas práticas. Outra medida importante é a integração entre as polícias. É comum os criminosos, em fuga, migrarem para outros Estados. De toda forma, a mudança da legislação garante que os criminosos tenham pena proporcional aos transtornos e perdas que este tipo de ataque pode gerar para a população.</p>

                        <p class="article"><em>Max Tavares Madruga, advogado e diretor jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</em></p>

