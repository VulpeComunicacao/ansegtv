                        <p class="article"> Diretores da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) se reuniram no dia 21 de março com o delegado Dr. Cristiano Campidelli, que assumiu, neste mês, a Coordenação-Geral na Diretoria de Polícia Administrativa da Polícia Federal em Brasília.</p>

                        <p class="article"> Na visita à sede da PF, representaram a entidade o Presidente Gabriel Damasceno, o Diretor Vice-Presidente, Eginaldo de Oliveira Silva, o Diretor Jurídico, Max Madruga, e o Diretor de Segurança, Oziel Carneiro.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2023/03/reuniao-pf.png" width="450" height="auto" alt="Reunião na PF" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article"> Além do Dr. Cristiano Campidelli, pela PF também estiveram presentes a Dra. Denise Tenorio, a Dra. Arryanne Vieira Queiroz e o Dr. Daniel Marques.</p>

                        <p class="article"> Mineiro de Itabira, o Dr. Campidelli atuou como chefe da Delegacia de Repressão a Crimes Contra o Meio Ambiente e Patrimônio Histórico da PF em Minas Gerais nos últimos anos. Também atuou como delegado regional da PF em Governador Valadares, onde desarticulou quadrilhas de tráfico de pessoas e imigração ilegal para os Estados Unidos.</p>

                        <p class="article"> Na reunião, o Dr. Campidelli se colocou à disposição da ANSEGTV para eventuais conversas e demandas envolvendo o setor de segurança e transporte de valores. </p>

                        <p class="article"> "Foi uma conversa bastante produtiva. Tivemos a oportunidade de apresentar a visão e os objetivos da nossa associação ao novo Coordenador. Além disso, falamos sobre a sinistralidade do setor nos últimos anos e sobre o futuro do nosso segmento. O Dr. Campidelli e sua equipe esclareceram que estão atentos ao que está acontecendo no mercado de transporte de valores e reforçou que a Polícia Federal tem uma visão de modernização das atividades de segurança privada", disse o Diretor Jurídico da ANSEGTV, Max Madruga.</p>
                            
