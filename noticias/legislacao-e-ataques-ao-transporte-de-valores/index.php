                        <div class="col-12 col-md-10 offset-md-1 content-news">

                        <p class="article">Empresas de transporte de valores são responsáveis pela proteção de milhões de reais que circulam pelo país e que permanecem sob custódia diariamente. São elas que abastecem o comércio, caixas eletrônicos e bancos. São seguranças, vigilantes e motoristas preparados, treinados, que se arriscam para atender a população da melhor forma possível. </p>


                        <p class="article">Vale recordar que a atividade foi considerada essencial desde o início da pandemia. Portanto, não parou em nenhum momento. Com o pagamento do auxílio emergencial, o deslocamento de dinheiro pelo país cresceu ainda mais. </p>


                        <p class="article">Infelizmente, tornou-se comum nos últimos anos acompanhar na mídia notícias sobre ataques cinematográficos contra o setor, com explosões, reféns e mortes. A investida contra um carro-forte, uma agência bancária ou uma base de transporte de valores não é algo fortuito. São ações planejadas, com um nível de organização maior que os crimes contra o patrimônio em geral e com um grande número de autores. </p>


                        <p class="article">Foram vários os casos, por exemplo, de cidades do interior invadidas por quadrilhas fortemente armadas, que causaram pânico na população. Fronteiras foram fechadas, com explosivos e fuzis capazes de derrubar tanques e aeronaves utilizados ostensivamente. </p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2020/11/max-tavares.png"" width="450" height="auto" alt="Max Tavares" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">Também ficaram marcados na história os mega-assaltos a terminais de cargas em aeroportos, com milhões de reais levados em dinheiro vivo, gerando pânico e grande prejuízo. </p>


                        <p class="article">E daí surge um círculo vicioso. Com as ações praticadas, os criminosos têm acesso a grandes quantias e conseguem se armar cada vez mais. Por outro lado, os gastos com proteção, seguro, melhorias de estrutura e material utilizados tornam-se vultuosos para bancos e transportadoras. </p>


                        <p class="article">Neste contexto, vale analisar a Lei de Crimes Hediondos (Lei 8.072), promulgada em 1990. De lá para cá, muita coisa mudou. Segundo sua definição, delitos desse tipo são aqueles de extrema gravidade e, em razão disso, recebem um tratamento diferenciado e mais rigoroso do que as demais infrações penais. São considerados ainda inafiançáveis e insuscetíveis de graça, anistia ou indulto. </p>


                        <p class="article">Dentre os vários crimes considerados hediondos, como o estupro e homicídio qualificado, está o furto qualificado com o emprego de explosivo ou de artefato análogo que cause perigo comum. Neste ponto, podemos observar que a legislação é falha ao não qualificar o roubo nas mesmas condições como hediondo, mas apenas o furto. Esta lacuna legal é prejudicial ao setor de transporte de valores, pois é um dos mais atingidos por ações criminosas desse gênero. </p>


                        <p class="article">Além do prejuízo financeiro e material, as perdas humanas e os traumas causados aos trabalhadores e suas famílias são irreparáveis. Isso sem falar nas terríveis consequências para toda a sociedade, com o aumento da criminalidade urbana. </p>


                        <p class="article">A atualização da norma jurídica é necessária. Com o passar dos anos, a sociedade mudou. Infratores se aperfeiçoaram e se especializaram. Obviamente, essa discussão deve ser ampla e reunir todos os setores da sociedade. </p>


                        <p class="article"><em>Max Tavares Madruga, advogado e diretor jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores). </em></p>


