                            <h1>7º Fórum CNT de Debates tem a participação da ANSEGTV</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> ANSEGTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>28/11/2023 - 10h25</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
