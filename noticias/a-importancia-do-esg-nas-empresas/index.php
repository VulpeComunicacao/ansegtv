                    <div class="row">
                        <div class="col-12 col-md-10 offset-md-1 content-news">
                            <p class="article">ESG, sem dúvidas, é um dos temas mais discutidos atualmente. A sigla, em inglês <em>environmental, social and corporate governance</em>, representa a maior preocupação das empresas com práticas relacionadas ao meio ambiente, responsabilidade social e governança.</p>
                            <p class="article">O interesse pelo assunto e a busca por um desempenho mais sustentável surgiu diante de uma maior cobrança do mercado, que passou a exigir de todos uma atenção especial ao impacto social que imprimem em suas atividades.</p>

                            <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/04/esg-ansegtv.jpg" width="450" height="auto" alt="Importância do ESG nas empresas" class=" img-fluid">	
                                <figcaption></figcaption>
                            </figure>
                            <p class="article">As normas que guiam o ESG não são imutáveis ou obrigatórias, podendo ser atualizadas a qualquer momento. Mas, segundo o Pacto Global, maior iniciativa de sustentabilidade corporativa do mundo, encabeçada pela ONU (Organização das Nações Unidas), dez princípios básicos desse recente conceito devem guiar qualquer empresa no mundo. São eles: 1) respeitar e apoiar os direitos humanos reconhecidos internacionalmente; 2) assegurar que a empresa não participe da violação dos direitos humanos; 3) apoiar a liberdade de associação e reconhecer o direito à negociação coletiva; 4) eliminar todas as formas de trabalho forçado ou compulsório; 5) erradicar todas as formas de trabalho infantil; 6) estimular práticas que eliminem qualquer tipo de discriminação no trabalho; 7) assumir práticas que adotem uma abordagem preventiva, responsável e proativa para os desafios ambientais; 8) desenvolver iniciativas para promover e disseminar a responsabilidade social; 9) incentivar o desenvolvimento e a difusão de tecnologias ambientalmente amigáveis; e 10) combater a corrupção em todas as suas formas, incluindo a extorsão e o suborno.</p>
                            <p class="article">No transporte de valores, não é diferente. Um exemplo são os programas de compensação ambiental. Cada vez mais empresas vêm adotando medidas que visam minimizar os efeitos da emissão de gases pelos veículos. Neste caso, árvores podem ser plantadas com o objetivo de recompensar em até 100% os possíveis danos ao ecossistema.</p>
                            <p class="article">Documentações e guias também caminham para uma maior digitalização, o que representa um importante corte de custos, menos burocracia e maior sustentabilidade.</p>
                            <p class="article">Programas de integridade, <em>compliance</em>, diversidade e o total respeito aos profissionais devem ser prioridades. A tecnologia também deve ser uma importante aliada, já que sua correta utilização representa modernidade, ganho de tempo e eficiência.</p>
                            <p class="article">Em tempos de pandemia, os conceitos de ESG ganham importância ainda maior. Empresas que buscam segui-los possuem maior capacidade de resiliência em meio a um período tão incerto e desafiador.</p>
