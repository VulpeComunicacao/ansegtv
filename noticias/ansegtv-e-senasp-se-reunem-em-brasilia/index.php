                        <p class="article">O Secretário Nacional de Segurança Pública, Mario Luiz Sarrubbo, e o Diretor de Operações Integradas e de Inteligência da Secretaria Nacional de Segurança Pública (SENASP), Rodney da Silva, receberam na quarta-feira (3) membros da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores) em Brasília. Representaram a entidade o Diretor Jurídico, Max Tavares Madruga, e o Secretário-Geral, Mauricio de Albuquerque Wanderley.</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2024/04/encontro-senasp.png" width="450" height="auto" alt="ENCONTRO com a SENASP - ANSEGTV" class=" img-fluid">	
