                        <p class="article"> Criatividade e adaptação. Essas são as palavras que se destacam ao ouvir o presidente da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Gabriel Damasceno, falar sobre o atual momento que o país atravessa.</p>

                        <p class="article"> Se no final de 2021 os números da pandemia de Covid-19 apresentaram queda, no início de 2022, com o surgimento da variante Ômicron, vemos novo crescimento de casos. </p>

                        <p class="article"> Na entrevista a seguir, apesar do momento econômico conturbado, Damasceno demonstra otimismo diante dos desafios que vêm pela frente.</p>

                        <p class="article"> <strong>Pergunta - Uma questão inevitável. De que forma o surgimento de novas variantes do coronavírus prejudica a atividade de transporte de valores e de segurança privada?</strong></p>

                        <p class="article"> <em>Resposta - As consequências são várias. Todos os setores sentem os efeitos da crise. Diante da instabilidade econômica, investimentos são adiados. Com a vacinação, imaginávamos que a situação se normalizaria. Não poderíamos prever que a pandemia tivesse um reaquecimento. Mas prefiro destacar a força das associadas. Estamos saindo do lugar comum, buscando soluções, lançando novos produtos. É preciso se reinventar, criar. Não é momento de se lamentar.</em></p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2022/02/gabriel-presidente-ansegtv.jpg" width="450" height="auto" alt="Gabriel Presidente TECBAN" class=" img-fluid">	
                            <figcaption></figcaption>
                        </figure>
                        
                        <p class="article"> <strong>Pergunta - Em ano eleitoral é sempre complicado aprovar reformas. Como avalia esse cenário?</strong></p>

                        <p class="article"> <em>Resposta - Realmente, o calendário é apertado. A disputa eleitoral é acirrada e polarizada. As chances de aprovar reformas significativas são pequenas. Mas a importância da reforma tributária, por exemplo, é inegável. Os impostos pagos pelas empresas são muito altos. Precisamos ter esperança, apesar das dificuldades que um ano atípico como esse trazem.</em></p>

                        <p class="article"> <strong>Pergunta - Qual a importância do PL que transforma ataques a carro-forte e bases crime hediondo?</strong></p>

                        <p class="article"> <em>Resposta - Sem dúvida é muito importante. Além dos mega-assaltos, os crimes conhecidos como “novo cangaço”, caracterizados quando dezenas de bandidos fortemente armados dominam temporariamente pequenas e médias cidades para o cometimento de roubos, também ocorrem contra carros-fortes e sedes de empresas transportadoras de valores. Em outubro de 2021, por exemplo, um assalto a carro-forte no Grajaú, em São Paulo, terminou com um tiroteio e um policial baleado. São inúmeras as ocorrências similares a esta que se noticiam, e o fundamental nessa questão é a atualização legislativa capaz de empregar maior rigor e punição para estes crimes.</em></p>

                        <p class="article"> <strong>Pergunta: A ANSEGTV é apoiadora da Frente Parlamentar do Serviços. Como será essa parceria em 2022?</strong></p>

                        <p class="article"> <em>Resposta - Seguimos apoiando e acompanhando os trabalhos da Frente. Serviços é o principal responsável pelo PIB. O grupo de deputados têm um papel bastante ativo, fundamental, defendendo as bandeiras do setor, promovendo reuniões com autoridades e crescendo cada vez mais. Certamente, nossa parceria tem tudo para se fortalecer. Ano passado foi de vários avanços, com a criação de grupos de trabalho específicos, divididos por setores. É um trabalho necessário.</em></p>

                        <p class="article"> <strong>Pergunta: Mesmo com o surgimento dos meios de pagamento eletrônicos, a preferência do brasileiro ainda é pelo dinheiro físico. A que o Sr. atribui esse movimento?</strong></p>

                        <p class="article"> <em>Resposta - O dinheiro físico segue com papel fundamental em nosso país, principalmente na parte da população mais carente. Meios eletrônicos e o dinheiro podem e devem conviver juntos para o desenvolvimento da economia do país.</em></p>

