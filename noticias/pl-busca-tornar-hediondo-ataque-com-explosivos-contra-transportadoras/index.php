                        <p class="article">O deputado federal Luiz Philippe de Orleans e Bragança (PSL-SP) protocolou nesta terça (21), na Câmara, o Projeto de Lei 3242/2021, que tem como objetivo atualizar a legislação para tornar hediondo os crimes de furto qualificado e de roubo quando envolvam o uso de explosivos para ataques contra empresas de transporte de valores e carros-fortes.</p>

                        <p class="article">Com isso, seriam alterados o Decreto-Lei nº 2.848, de 7 dezembro de 1940 (Código Penal), e a Lei nº 8.072 (Lei de Crimes Hediondos), de 25 de julho de 1990. O PL foi uma sugestão da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/09/plenario.jpg" width="450" height="auto" alt="Plenário" class=" img-fluid">	
                            <figcaption>José Cruz/Agência Brasil</figcaption>
                        </figure>

                        <p class="article">Segundo o texto, a previsão da pena de reclusão no caso da subtração de bens e valores transportados com o emprego de explosivo ou de artefato análogo varia de quatro a dez anos e multa.</p>

                        <p class="article">O ataque agressivo e brutal às empresas dos setores de segurança privada, transporte de valores e segmentos correlatos é algo recorrente no país. Nos últimos anos, porém, esse tipo de crime cresceu, especialmente, contra guardas de valores e carros-fortes. Tais ações tiram vidas de trabalhadores, treinados para garantir a logística de numerário no país. 
</p>

                        <p class="article">Já sabe-se que os valores roubados servem de financiamento para o crime organizado, que adquire cada vez mais armamentos pesados. Os roubos também prejudicam o abastecimento da população, atrapalhando a produtividade da economia brasileira. Sem falar no aumento de custos para a contratação ou renovação da cobertura securitária das transportadoras.</p>
                      
                        <p class="article">A revisão da lei é uma proeminente demanda da ANSEGTV. A entidade seguirá atenta ao tema, acompanhando o trâmite do PL e dialogando sempre com a sociedade.</p>
