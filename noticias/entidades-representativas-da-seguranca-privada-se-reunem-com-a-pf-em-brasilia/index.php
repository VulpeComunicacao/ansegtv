                        <p class="article">O Diretor Jurídico da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Max Tavares Madruga, participou na terça-feira (17) do II Encontro com as Entidades Representativas do segmento de Segurança Privada, na sede da Polícia Federal, em Brasília (DF).</p>

                        <p class="article">O convite para a reunião partiu do Delegado de Polícia Federal e Coordenador-Geral de Controle de Serviços e Produtos, Dr. Cristiano Jomar Costa Campidelli. </p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2023/10/reuniao-com-pf.png" width="450" height="auto" alt="Reunião com Polícia Federal" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">Na pauta, foram tratados principalmente temas que constam na Portaria nº 18.045/2023-DG/PF, publicada em abril. </p>

                        <p class="article">De forma prioritária, as inovações sobre a obrigatoriedade do abastecimento com cassete lacrado e da manutenção técnica de caixas eletrônicos com equipe completa de vigilantes dominaram os debates. As entidades puderam defender seus pontos de vista, que envolviam a segurança física dos profissionais, a complexidade de implantação das medidas e os possíveis reflexos para os serviços de transporte de valores.</p>

                        <p class="article">Max elogiou a qualidade do encontro. “Mais uma vez, a Polícia Federal mostra disposição e abertura ao diálogo. Essa troca de informações e experiências é de vital importância. A ANSEGTV estará sempre pronta para discutir medidas que visem o aperfeiçoamento do setor de segurança”.</p>
            
