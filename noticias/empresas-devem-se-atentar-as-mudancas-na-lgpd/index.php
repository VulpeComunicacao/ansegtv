                        <div class="col-12 col-md-10 offset-md-1 content-news">

                        <p class="article">A Lei Geral de Proteção de Dados Pessoais (LGPD), em vigor desde setembro de
                        2020, passou a ter suas sanções administrativas aplicáveis a partir de 1º de
                        agosto. No entanto, as sanções ainda dependem de regulamentação da
                        Autoridade Nacional de Proteção de Dados (ANPD), órgão responsável pela
                        fiscalização e autuação das empresas em desacordo com a legislação.</p>



                        <p class="article">Embora o cálculo para definição da pena ainda esteja em definição, as multas
                        podem chegar a 2% do faturamento, limitado ao teto de R$ 50 milhões, bem
                        como o banco de dados pode ser bloqueado, impedindo o funcionamento da
                        empresa.</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/08/lock-lgpd.jpg" width="450" height="auto" alt="Cadeado de dados" class=" img-fluid">	
                            <figcaption></figcaption>
                        </figure>



                        <p class="article">As empresas precisam dar prioridade à jornada de adequação à LGPD. Em
                        especial, estabelecer procedimentos para atender aos direitos dos titulares,
                        como o direito de acessar os seus dados pessoais e solicitar a eliminação de
                        dados tratados em excesso. Precisam, ainda, nomear um encarregado e
                        estabelecer um protocolo de reação a incidentes de segurança, sejam esses
                        ligados aos próprios empregados ou às empresas parceiras.</p>



                        <p class="article">Para isto, é necessário estarem atentas a uma coleta de dados limitada à
                        finalidade de suas atividades. Além destas medidas, é preciso ter precaução
                        no compartilhamento dos dados com terceiros: se a empresa não tiver
                        obrigação legal e não informar na sua política sobre este compartilhamento,
                        não poderá fazê-lo.</p>



                        <p class="article">Também é vital que as organizações estabeleçam um programa de adequação
                        robusto que permita o amplo mapeamento dos tratamentos realizados pela
                        organização e a elaboração de uma política de privacidade com treinamentos
                        específicos para mitigar riscos.</p>



                        <p class="article">Por fim, a empresa precisa certificar-se de que, caso o titular do dado
                        solicite a exclusão ou alteração dessas informações da base de registros da
                        empresa, é necessário que ela tenha a condição de cumpri-lo. E mais:
                        apresentar prova de que foi feito, caso necessário.</p>

                      
