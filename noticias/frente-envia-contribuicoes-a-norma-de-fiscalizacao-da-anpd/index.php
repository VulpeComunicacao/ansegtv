                        <p class="article">A Frente Parlamentar do Setor de Serviços (FPS), por meio do Observatório de Políticas Setoriais (OPS), enviou no dia 28 de junho suas contribuições à Norma de Fiscalização da ANPD (Autoridade Nacional de Proteção de Dados do Brasil), colocada em consulta pública.</p>

                        <p class="article">Criada em outubro de 2019 pelo deputado federal Laércio Oliveira (PP-SE), a FPS conta com o apoio da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores).</p>

                        <figure class="pull-right m-2 text-left">
                                <img src="../../img/uploads/2021/07/fiscalizacao-anpd-share.jpg" width="450" height="auto" alt="Fiscalização ANPD" class=" img-fluid">	
                                <figcaption></figcaption>
                        </figure>

                        <p class="article">Sem fins lucrativos, o OPS tem o intuito de proporcionar o fortalecimento institucional das entidades apoiadoras da FPS junto aos órgãos públicos e autoridades que detêm o poder de decisão político e regulatório no âmbito dos Poderes Legislativo e Executivo.</p>

                        <p class="article">No total, foram 46 apontamentos, entre preocupações e sugestões de melhorias. O trabalho foi elaborado pelo Grupo de Trabalho em LGPD (Lei Geral de Proteção de Dados Pessoais) da FPS, sob coordenação do advogado Fabricio da Mota Alves.</p>

                        <p class="article">Resumidamente, as considerações visam uma maior transparência para critérios de decisões da ANPD e uma melhor definição de parâmetros para sua intervenção em empresas.</p>

                        <p class="article">A LGPD foi sancionada pelo presidente Jair Bolsonaro em setembro de 2020. A partir de agosto, a ANPD poderá aplicar as primeiras multas com base na norma de dados pessoais.</p>
  
