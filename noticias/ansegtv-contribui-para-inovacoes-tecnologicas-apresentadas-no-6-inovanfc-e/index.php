                        <p class="article">O 6º Inova NFC-e com o 1º Fórum DF-e Transportes debateu na tarde desta quarta-feira, 28/7, as gestões fiscais e suas soluções em tecnologias somadas à Nota Fiscal de Consumidor Eletrônica e aos Documento Fiscais Eletrônicos.  </p>

                        <p class="article">O evento contou com empresas e especialistas que apresentaram cases de sucesso e soluções inovadoras e tecnológicas na área de documentos fiscais eletrônicos, transportes, varejo, delivery e logística de cargas.</p>

                        <p class="article">A ANSEGTV  (Associação Nacional de Segurança e Transporte de Valores) acompanhou o evento e foi representada por Andrey Ferreira, assessor tributário da entidade, que também integrou a Comissão Julgadora dos cases explanados.</p>

                        <figure class="pull-left m-2 text-left">
                            <img src="../../img/uploads/2021/07/evento-inova-29-07.jpg" width="450" height="auto" alt="Evento Inova NFC-e " class=" img-fluid">	
                            <figcaption></figcaption>
                        </figure>

                        <p class="article">Um dos cases apresentados foi sobre a implementação da GTV-e  (Guia de Transporte de Valores Eletrônica) e sua eficiência para o rastreamento dos serviços.</p>
                        
                        <p class="article">A Guia é um dos documentos que compõe todo registro da mercadoria - origem/ destino, características e valores em transporte - logo, é um sistema eletrônico que permite estabelecer lastro a rastreabilidade, suporte para gerar faturamento e apuração de impostos. É uma solução que traz agilidade a operação a partir do momento que essas informações são disponibilizadas para toda uma cadeia.  </p>

                        <p class="article">O seminário foi uma realização do ENCAT (Encontro Nacional de Coordenadores e Administradores Tributários Estaduais), organizado pela AFRAC (Associação Brasileira de Tecnologia para o Comércio e Serviços) e que tem ainda o apoio institucional do GS1 Brasil (Associação Brasileira de Automação).</p>

                        <p class="article">A íntegra do evento está disponível no canal do 6º Inova NF-e no YouTube: <a href="https://www.youtube.com/watch?v=dKT1NvKYYic" target="_blank">https://www.youtube.com/watch?v=dKT1NvKYYic</a></p>
                      
