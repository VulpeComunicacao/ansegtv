                            <h1>Presidente da ANSEGTV destaca capacidade das empresas em enfrentarem a crise</h1>
                            <p class="post-infos">
                                <span class="mx-2 author"><i class="fa fa-user"></i> AnsegTV</span> | <span class="mx-2 date"><i class="fa fa-clock-o"></i>09/02/2022 - 12h15</span> | <span class="mx-2 category"><i class="fa fa-tag"></i> ANSEGTV</span>
