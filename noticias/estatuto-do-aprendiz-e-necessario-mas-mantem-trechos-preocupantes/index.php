                            <p class="article">Após idas e vindas, a comissão especial que analisa a proposta que cria o Estatuto do Aprendiz (PL 6461/19) cancelou a votação do parecer do deputado Marco Bertaiolli (PSD-SP), prevista para ocorrer ainda em dezembro. Sem consenso, a discussão segue sem previsão de conclusão.</p>

                            <p class="article">O parlamentar apresentou um primeiro parecer sobre o assunto no início de novembro e recomendou a aprovação da matéria na forma de um substitutivo. Depois disso, foram apresentadas mais de 100 emendas sugerindo modificações no texto. Parte delas foi acatada, Bertaiolli apresentou novo parecer, mas ainda assim o assunto não tem consenso na comissão.</p>

                            <figure class="pull-right m-2 text-right">
                                <img src="../../img/uploads/2022/12/carteira-trabalho.png" width="450" height="auto" alt="Carteira de Trabalho" class=" img-fluid">	
                                <figcaption>Foto: Pedro Ventura - Agência Brasil</figcaption>
                            </figure>

                            <p class="article">Hoje, as empresas podem contratar como aprendizes entre 5% e 15% de seus trabalhadores, cujas funções demandam formação profissional. Pelo parecer de Bertaiolli, esse percentual passa a ser de 4% a 15% do total de trabalhadores existentes em cada estabelecimento, sem especificar a formação profissional.</p>

                            <p class="article">O projeto é importante por trazer inovações no processo de contratação dos jovens. A proposta, no entanto, traz alguns trechos que poderiam colocar em risco esses profissionais, principalmente em atividades do setor de segurança e transporte de valores.</p>

                            <p class="article">Em maio, o Diretor de Relações Governamentais da ANSEGTV (Associação Nacional de Segurança e Transporte de Valores), Lincoln Telhado, falou durante a audiência pública sobre o assunto na Câmara. Na ocasião, ele destacou que o setor, por exercer trabalho perigoso, teria dificuldade em cumprir a cota prevista na proposta, que não leva em conta as funções existentes nas empresas.</p>

                            <p class="article">Telhado lembrou, por exemplo, que adolescentes não poderiam andar em carros-fortes ou acompanhar seguranças armados.</p>

                            <p class="article">“Esse segue sendo nosso posicionamento. Apoiamos o Estatuto, mas entendemos como necessário que sejam excluídas da base de cálculo as atividades perigosas nos termos da lei”, diz Lincoln.</p>
       
